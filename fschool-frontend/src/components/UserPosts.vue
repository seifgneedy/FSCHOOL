<template>
  <v-container fluid>
    <v-card-title class="text-h3 text--primary font-weight-bold">
      {{ postType[0].toUpperCase() + postType.substring(1) + "s" }}
    </v-card-title>
    <v-row justify="end">
      <v-dialog v-model="postdialog" persistent :retain-focus="false" max-width="600px">
        <template v-slot:activator="{ on, attrs }">
          <v-btn
            color="primary"
            dark
            v-bind="attrs"
            v-on="on"
            @click="addpostError = false"
          >
            New {{ postType[0].toUpperCase() + postType.substring(1) }}
          </v-btn>
        </template>
        <v-card>
          <v-card-title>
            <span class="text-h5">NEW POST</span>
          </v-card-title>
          <v-card-text>
            <v-container>
              <v-row>
                <v-col cols="12" sm="6" md="4">
                  <div v-if="userRole == 'teacher'">
                    <v-select
                      label="post type*"
                      :items="['announcement', 'post', 'question']"
                      :error-messages="typeError"
                      required
                      @change="$v.newpost.type.$touch()"
                      @blur="$v.newpost.type.$touch()"
                    ></v-select>
                  </div>
                  <div v-if="userRole == 'student'">
                    <v-select
                      label="post type*"
                      :items="['post', 'question']"
                      :error-messages="typeError"
                      required
                      @change="$v.newpost.type.$touch()"
                      @blur="$v.newpost.type.$touch()"
                    ></v-select>
                  </div>
                </v-col>
                <v-col cols="12" sm="6" md="4"> </v-col>
                <v-col cols="12" sm="6" md="4">
                  <v-text-field
                    name="Title/summary*"
                    label="Title/summary*"
                    :error-messages="titleError"
                    v-model="newpost.title"
                    @input="$v.newpost.title.$touch()"
                    @blur="$v.newpost.title.$touch()"
                    required
                  ></v-text-field>
                </v-col>
                <v-col cols="12">
                  <v-textarea
                    name="post body"
                    label="post body"
                    :error-messages="postBodyError"
                    v-model="newpost.body"
                    outlined
                    type="text"
                    @input="$v.newpost.body.$touch()"
                    @blur="$v.newpost.body.$touch()"
                    required
                  ></v-textarea>
                </v-col>
              </v-row>
            </v-container>
            <small>*indicates required field</small>
          </v-card-text>
          <v-card-actions>
            <v-alert
              v-show="addpostError"
              type="error"
              style="font-size: 19px; font-weight: bold"
              >Error adding post:{{ postError }}</v-alert
            >
            <v-spacer></v-spacer>
            <v-btn color="blue darken-1" text @click="closePostDialog()"> Cancle </v-btn>
            <v-btn color="blue darken-1" text @click="addpost()"> Add </v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
    </v-row>
    <v-col v-for="post in posts" :key="post.code">
      <v-card class="mx-auto" color="#0F0639" dark max-width="600">
        <v-card-title>
          <v-icon large left> </v-icon>

          <span class="text-h6 font-weight-light">"Post Title"</span>
        </v-card-title>

        <v-card-text class="text-h5 font-weight-bold">
          "Post body"
        </v-card-text>

        <v-card-actions>
          <v-list-item class="grow">
            <v-list-item-avatar color="grey darken-3">
              <v-img class="elevation-6" src="@/assets/publisher.svg"></v-img>
            </v-list-item-avatar>

            <v-card-action>
              <v-text color="deep-purple lighten-2" text>
                user@schoole.com
              </v-text>
            </v-card-action>
            <v-row align="center" justify="end">
              <span class="subheading mr-2">"time"</span>
              <span class="mr-1">||</span>
              <v-icon class="mr-1" @click="openPost(post.code)">
                mdi-comment-multiple-outline
              </v-icon>
            </v-row>
            <v-dialog
              v-model="showComments"
              max-width="800px"
              :retain-focus="false"
            >
              <v-card align="center">
                <v-col v-for="comment in comments" :key="comment.code">
                  <v-card class="mx-auto" color="#0F0639" dark max-width="600">
                    <v-card-text class="text-h5 font-weight-bold">
                      "Comment body"
                    </v-card-text>
                    <v-card-actions>
                      <v-list-item class="grow">
                        <v-list-item-avatar color="grey darken-3">
                          <v-img
                            class="elevation-6"
                            src="@/assets/publisher.svg"
                          ></v-img>
                        </v-list-item-avatar>
                        <v-card-action>
                          <v-text color="deep-purple lighten-2" text>
                            user@fschool.com
                          </v-text>
                        </v-card-action>
                      </v-list-item>
                    </v-card-actions>
                  </v-card>
                </v-col>
                <v-card-actions>
                  <v-spacer />
                  <v-btn color="blue darken-1" dark @click="closePost()"
                    >Close</v-btn
                  >
                  <v-spacer />
                   <v-row justify="end">
      <v-dialog v-model="commentdialog" :retain-focus="false" max-width="600px">
        <template v-slot:activator="{ on, attrs }">
          <v-btn
            color="primary"
            dark
            v-bind="attrs"
             @click="addCommentError = false"
            v-on="on"
          >
           Add comment
          </v-btn>
        </template>
        <v-card>
          <v-card-title>
            <span class="text-h5 " >NEW COMMENT</span>
          </v-card-title>
          <v-card-text>
            <v-container>
              <v-row>
                <v-col cols="12">
                  <v-textarea
                    name="comment body*"
                    label="comment body*"
                    :error-messages="commentBodyError"
                    v-model="newcomment.body"
                    outlined
                    type="text"
                    @input="$v.newcomment.body.$touch()"
                    @blur="$v.newcomment.body.$touch()"
                    required
                  ></v-textarea>
                </v-col>
              </v-row>
            </v-container>
            <small>*indicates required field</small>
          </v-card-text>
          <v-card-actions>
            <v-alert
              v-show="addCommentError"
              type="error"
              style="font-size: 19px; font-weight: bold"
              >Error adding post:{{ CommentError }}</v-alert
            >
            <v-spacer></v-spacer>
            <v-btn color="blue darken-1" text @click="closeCommentDialog()"> Cancle </v-btn>
            <v-btn color="blue darken-1" text @click="addComment()"> Add </v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
    </v-row>
                  <v-spacer />
                </v-card-actions>
              </v-card>
            </v-dialog>
          </v-list-item>
        </v-card-actions>
      </v-card>
    </v-col>
  </v-container>
</template>
<script>
import { AXIOS } from "../http-common.js";
import { required, email } from "vuelidate/lib/validators";
export default {
  props: ["postType", "userRole", "courseCode", "userEmail"],
  components: {},
  data: () => ({
    showComments: false,
    posts: [],
    postdialog: false,
    commentdialog:false,
    postError: "",
    CommentError:"",
    addpostError: false,
    addCommentError:false,
    currentPost:"",
    newpost: {
      type: "",
      title: "",
      body: "",
      email: "",
      Coursecode: "",
    },
    newcomment: {
      body: "",
      postid: "",
      email: "",
    },
     defultnewcomment: {
      body: "",
      postid: "",
      user: "",
    },
    defultnewpost: {
      type: "",
      title: "",
      body: "",
      email: "",
      Coursecode: "",
    },
  }),
  validations: {
    newpost: {
      type: {
        required,
      },
      title: {
        required,
      },
      body: {
        required,
      },
      email: {
        required,
        email,
      },
      Coursecode: {
        required,
      },
    },
    newcomment: {
      body: {
        required,
      },
    },
  },

  created() {
    this.initialize();
  },

  methods: {
    async initialize() {
      //TODO get this course's posts instead
      // Getting  courses
      await AXIOS.get("admin/courses", {}).then((res) => {
        this.posts = res.data;
      });
    },
    async openPost(postId) {
      //get post comments
      await AXIOS.get("admin/courses", {}).then((res) => {
        this.comments = res.data;
      });
      this.showComments = true;
      this.currentPost = postId;
    },
    //sending posts
    async addpost() {
      this.$v.$touch();
      if (
        this.$v.newpost.type.$invalid ||
        this.$v.newpost.title.$invalid ||
        this.$v.newpost.body.$invalid
      )
        return;
      this.newpost.email = this.userEmail;
      this.newpost.Coursecode = this.CourseCode;
      let response,
        networkError = false;
      await AXIOS.post("admin/user", this.newpost)
        .then((res) => {
          response = res.data;
        })
        .catch(() => {
          networkError = true;
          response = 0;
        });
      if (response != 0) {
        this.close();
      } else if (networkError) {
        this.addpostError = true;
        this.postError = "Network Error,Try Again";
      }
    }, 
    //sending comments
    async addComment() {
     this.$v.$touch();
     if (
        this.$v.newcomment.body.$invalid
      )
        return;
        this.newcomment.email = this.userEmail;
        this.newcomment.postid = this.currentPost;
         let response,
        networkError = false;
      await AXIOS.post("admin/user", this.newcomment)
        .then((res) => {
          response = res.data;
        })
        .catch(() => {
          networkError = true;
          response = 0;
        });
      if (response != 0) {
        this.closeCommentDialog();
      } else if (networkError) {
        this.CommentError = true;
        this.CommentError = "Network Error,Try Again";
      }
    },
    closePost() {
      this.showComments = false;
    },
    closeCommentDialog() {
       this.$v.$reset();
      this.commentdialog = false;
      this.$nextTick(() => {
        this.newcomment = Object.assign({}, this.defultnewcomment);
      });
    },
    closePostDialog() {
       this.$v.$reset();
      this.postdialog = false;
      this.$nextTick(() => {
        this.newpost = Object.assign({}, this.defultnewpost);
      });
    },
  },
    computed: {
    typeError() {
      const errors = [];
      if (!this.$v.newpost.type.$dirty) return errors;
      !this.$v.newpost.type.required &&
        errors.push("type required");
      return errors;
    },
    titleError() {
      const errors = [];
       if (!this.$v.newpost.title.$dirty) return errors;
      !this.$v.newpost.title.required &&
        errors.push("title required");
      return errors;
     
    },
    postBodyError() {
      const errors = [];
       if (!this.$v.newpost.body.$dirty) return errors;
      !this.$v.newpost.body.required &&
        errors.push("body required");
      return errors;
    },
     commentBodyError() {
      const errors = [];
       if (!this.$v.newcomment.body.$dirty) return errors;
      !this.$v.newcomment.body.required &&
        errors.push("body required");
      return errors;
    },
  },
};
</script>
